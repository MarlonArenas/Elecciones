    private void botonActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonActualizarActionPerformed

        String numeroDocumento = this.campoDocumento.getText();
        String nombre = this.campoNombre.getText();
        String correo = this.campoCorreo.getText();
        String telefono = this.campoTelefono.getText();
        String ciudadOrigen = this.campoCiudadOrigen.getText();
        String partidoPolitico = this.campoPartidoPolitico.getText();
        String mensajeCampania = this.campoMensajeCampania.getText();
        String descripcion = this.campoDescripcion.getText();
        String propuestas = this.campoPropuestas.getText();
        String tipoDocumento = this.comboTipoDocumento.getSelectedItem().toString();

        ClsCandidato candidato = new ClsCandidato(partidoPolitico, ciudadOrigen,
                descripcion, mensajeCampania, propuestas, tipoDocumento, numeroDocumento, nombre, telefono, correo);

        ClsMensaje mensaje = this.controladorCandidato.actualizarCandidato(candidato);

        if (mensaje.getTipo().equals(ClsMensaje.OK)) {
            obtenerCandidatos();
        }

        JOptionPane.showMessageDialog(rootPane, mensaje.getTexto());
    }//GEN-LAST:event_botonActualizarActionPerformed

    public void obtenerCandidatos() {

        this.listaCandidatos = this.controladorCandidato.obtenerCandidatos();
        this.actualizarTablaCandidato(listaCandidatos);
    }

    public void actualizarTablaCandidato(LinkedList<ClsCandidato> candidatos) {

        DefaultTableModel Modelo = (DefaultTableModel) this.tablaCandidatos.getModel();
        Modelo.setRowCount(0);
        for (ClsCandidato candidato : candidatos) {
            Object[] fila = {candidato.getNumeroDocumento(), candidato.getNombre(), candidato.getTelefono(),
                candidato.getCorreo(), candidato.getPartidoPolitico(), candidato.getCiudadOrigen(), candidato.getDescripcion(), candidato.getMensajeCampania(),
                candidato.getPropuestas(), candidato.getTipoDocumento()};
            Modelo.addRow(fila);
        }

    }

