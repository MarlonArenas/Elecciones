    private void botonEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonEliminarActionPerformed

        int columna = 0;
        int row = this.tablaCandidatos.getSelectedRow();
        String id = this.tablaCandidatos.getValueAt(row, columna).toString();

        ClsMensaje mensaje = this.controladorCandidato.eliminarCandidato(id);
        if (mensaje.getTipo().equals(ClsMensaje.OK)) {
            obtenerCandidatos();

        }
        mensaje.mostrarMensaje();
    }//GEN-LAST:event_botonEliminarActionPerformed

    public ClsCandidato buscarCandidato(String idCandidato) {

        for (ClsCandidato candidato : this.listaCandidatos) {
            if (idCandidato.equals(candidato.getNumeroDocumento())) {
                return candidato;
            }
        }
        return null;
    }

    private void botonEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonEditarActionPerformed
        this.panelPestanias.setSelectedIndex(1);

        int row = this.tablaCandidatos.getSelectedRow();
        int columna = 0;
        String id = this.tablaCandidatos.getValueAt(row, columna).toString();

        ClsCandidato candidato = this.buscarCandidato(id);
        

        if (candidato != null) {

            this.campoDocumento.setEnabled(false);
            this.botonAgregar.setVisible(false);

            this.botonActualizar.setVisible(true);
            this.botonNuevo.setVisible(true);

            this.campoDocumento.setText(candidato.getNumeroDocumento());
            this.campoNombre.setText(candidato.getNombre());
            this.campoTelefono.setText(candidato.getTelefono());
            this.campoCorreo.setText(candidato.getCorreo());
            this.campoPartidoPolitico.setText(candidato.getPartidoPolitico());
            this.campoCiudadOrigen.setText(candidato.getCiudadOrigen());
            this.campoDescripcion.setText(candidato.getDescripcion());
            this.campoMensajeCampania.setText(candidato.getMensajeCampania());
            this.campoPropuestas.setText(candidato.getPropuestas());
            this.comboTipoDocumento.setSelectedItem(candidato.getTipoDocumento());

        }
    }//GEN-LAST:event_botonEditarActionPerformed


    private void botonAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAgregarActionPerformed

        String numeroDocumento = this.campoDocumento.getText();
        String nombre = this.campoNombre.getText();
        String telefono = this.campoTelefono.getText();
        String correo = this.campoCorreo.getText();
        String partidoPolitico = this.campoPartidoPolitico.getText();
        String ciudadOrigen = this.campoCiudadOrigen.getText();
        String descripcion = this.campoDescripcion.getText();
        String mensajeCampania = this.campoMensajeCampania.getText();
        String propuestas = this.campoPropuestas.getText();
        String tipoDocumento = this.comboTipoDocumento.getSelectedItem().toString();

        ClsCandidato candidato = new ClsCandidato(partidoPolitico, ciudadOrigen,
                descripcion, mensajeCampania, propuestas, tipoDocumento, numeroDocumento, nombre, telefono, correo);

        ClsMensaje mensaje = this.controladorCandidato.agregarCandidato(candidato);
        if (mensaje.getTipo().equals(ClsMensaje.OK)) {
            JOptionPane.showMessageDialog(rootPane, mensaje.getTexto());
            obtenerCandidatos();
            limpiarFormulario();

        }
    }//GEN-LAST:event_botonAgregarActionPerformed

